/*5. Write a program to print the first 10 even natural numbers in reverse order.*/

#include<stdio.h>
#include<conio.h>

int main()
{
	int num = 20;
	
	while(num >= 1)
	{
		if(num % 2 == 0)
		{
			printf("%d\n",num);
		}
		num--;
	}
	getch();
}
