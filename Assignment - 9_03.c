/*3. Write a program to print the first 10 natural numbers in reverse order.*/

#include<stdio.h>
#include<conio.h>

int main()
{
	int num = 10;
	
	while(num >= 1)
	{
		printf("%d\n",num);
		num--;
	}
	getch();
}
