/*10. Write a program to print the cubes of the first 10 natural numbers in reverse order.*/

#include<stdio.h>
#include<conio.h>
#include<math.h>
int main()
{
	for(int i = 10; i >= 1; i--)
	{
		int cube = pow(i,3);
		
		printf("%d\n",cube);
	}
	getch();
}
