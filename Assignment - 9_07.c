/*7. Write a program to print the squares of first 10 natural numbers*/

#include<stdio.h>
#include<conio.h>
#include<math.h>
int main()
{
	int num = 1;
	
	while(num <= 10)
	{
		int square = pow(num,2);
		printf("%d\n",square);
		
		num++;
	}
	getch();
}
